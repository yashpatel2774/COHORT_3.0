// Q-1 Check whether a number is even or odd using ternary operator.

var num = 10;

console.log(num % 2 === 0 ? "Even" : "Odd"); /// Even

// Q-2 Check whether age is above 18 using ternary operator.

var age = 20;
console.log(age > 18 ? "Above 18" : "Below 18"); // Above 18

// Q-3 Find the greater number between two values using ternary operator.

var num1 = 23;
var num2 = 60;

console.log(num1 > num2 ? num1 : num2); // 60
